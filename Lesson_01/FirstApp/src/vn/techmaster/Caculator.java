package vn.techmaster;

public class Caculator {
    public void phepcong(double a, double b) {
        System.out.println("*Bài Tập 1* \nThực hiện các phép toán: \n*Cộng - Trừ - Nhân - Chia*");
        System.out.println("Tổng 2 số là: " + (a + b));
    }

    public void pheptru(double a, double b) {
        System.out.println("Hiệu 2 số là: " + (a - b));
    }

    public void phepnhan(double a, double b) {
        System.out.println("Tích 2 số là: " + (a * b));
    }

    public void phepchia(double a, double b) {
        System.out.println("Thương 2 số là: " + (a / b));
        System.out.println("-----------------------------");
    }
}





